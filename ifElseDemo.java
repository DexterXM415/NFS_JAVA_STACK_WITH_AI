public class ifElseDemo {
    public static void main(String[] args) {
        int marks = 75;
        if (marks > 50) {
            System.out.println("You are pass");
        } else if (marks >= 50) {
            System.out.println("You barely passed");
        } else {
            System.out.println("You are fail");
        }
    }
}
