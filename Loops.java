public class Loops {
    public static void main(String[] args) {
        for (int i = 1; i < 5; i++) {
            System.out.println("For loop iteration:" + i);
        }

        int i = 1;
        while (i <= 5) {
            System.out.println("While loop iteration:" + i);
            i++;
        }
    }
}
